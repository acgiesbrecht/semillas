    private void cboMensualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboMensualActionPerformed
        try {
            Connection conn = DriverManager.getConnection("jdbc:postgresql://192.168.3.109:5432/industria_lab", "postgres", "123456");
            Map parameters = new HashMap();
            parameters.put("pAno", Integer.parseInt(txtAno.getText()));
            parameters.put("pMes", Integer.parseInt(txtMes.getText()));
            parameters.put("gobierno_nacional", getClass().getResourceAsStream("/reports/gobierno_nacional.jpg"));
            parameters.put("senave_logo", getClass().getResourceAsStream("/reports/senave_logo.jpg"));

            JasperReport report = JasperCompileManager.compileReport(getClass().getResourceAsStream("/reports/InformeMensualSemillas.jrxml"));

            JasperPrint jasperPrint = JasperFillManager.fillReport(report, parameters, conn);
            JasperViewer jReportsViewer = new JasperViewer(jasperPrint, false);
            jReportsViewer.setVisible(true);
            //JasperPrintManager.printReport(jasperPrint, false);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, Thread.currentThread().getStackTrace()[1].getMethodName() + " - " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cboMensualActionPerformed

